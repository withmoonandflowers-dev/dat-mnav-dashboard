export function t(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let text = translations[locale]?.[key] || translations['en'][key] || key
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v))
    })
  }
  return text
}

// Map locale to Claude AI analysis language instruction
export function getAiLanguageInstruction(locale: Locale): string {
  switch (locale) {
    case 'zh-TW': return 'Please respond entirely in Traditional Chinese (繁體中文).'
    case 'zh-CN': return 'Please respond entirely in Simplified Chinese (简体中文).'
    case 'ja': return 'Please respond entirely in Japanese (日本語).'
    default: return 'Please respond in English.'
  }
}
